// operator functions

function add(value1, value2){
    let result = value1 + value2;
    console.log(result);
};

function subtract(value1, value2){
    let result = value1 - value2;
    console.log(result);
};

function multipy(value1, value2){
    let result = value1 * value2;
    console.log(result);
};

function divide(value1, value2){
    let result = value1 / value2;
    console.log(result);
};

